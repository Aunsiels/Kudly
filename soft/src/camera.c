void frameEndCb(DCMIDriver* dcmip) {
   (void) dcmip;
   chSysLockFromIsr();
   chEvtBroadcastI(&es2);
   chEvtBroadcastI(&es1);
   chSysUnlockFromIsr();
}

void dmaTxferEndCb(DCMIDriver* dcmip) {
   (void) dcmip;
   chSysLockFromIsr();
   chEvtBroadcastI(&es1);
   chSysUnlockFromIsr();
}

